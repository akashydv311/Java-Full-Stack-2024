package patternprograms;

public class Tester {
	public static void main(String[] args) {
		
		// While Loop
		int i=0;
		while(i<10) {
			System.out.print("* ");
			i++;
		}
		System.out.println();
		
		// do while Loop
		i=0;
		do {
			System.out.print("% ");
			i++;
		}while(i<10);
		System.out.println();
		
		// For Loop
		for(i=0; i<10; i++) {
			System.out.print("@ ");
		}
		
	}
	
}
